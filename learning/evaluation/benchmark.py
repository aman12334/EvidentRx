"""
Benchmark suite persistence.

Manages versioned benchmark suites used by the evaluation harness.
A benchmark is a curated, labelled dataset of investigation cases
with ground-truth outputs.

Benchmark quality guarantees
─────────────────────────────
  - Immutable once published (new versions create new benchmark records)
  - Source-traced (every case tracks its origin: real investigation or synthetic)
  - PHI-safe (patient identifiers replaced with stable synthetic IDs)
  - Version-locked (evaluations reference the exact benchmark version used)
"""

from __future__ import annotations

import hashlib
import json
import logging
import uuid
from dataclasses import dataclass, field
from datetime    import datetime, timezone
from enum        import Enum
from typing      import Any, Callable, Optional

from learning.evaluation.harness import EvaluationCase

log = logging.getLogger("evidentrx.learning.evaluation.benchmark")


class BenchmarkStatus(str, Enum):
    DRAFT     = "draft"
    PUBLISHED = "published"
    DEPRECATED= "deprecated"
    ARCHIVED  = "archived"


class CaseSource(str, Enum):
    REAL_ANONYMISED   = "real_anonymised"   # from real investigation, PHI removed
    SYNTHETIC         = "synthetic"          # generated by SDK simulator
    EXPERT_CRAFTED    = "expert_crafted"     # manually created by compliance expert
    ADVERSARIAL       = "adversarial"        # designed to expose edge cases


@dataclass
class BenchmarkCase:
    """A labelled evaluation case within a benchmark suite."""
    case_id:       str
    source:        CaseSource
    category:      str                   # "false_positive" | "escalation" | "remediation" | …
    difficulty:    str                   # "easy" | "medium" | "hard" | "adversarial"
    evaluation_case: EvaluationCase
    created_at:    datetime              = field(default_factory=lambda: datetime.now(tz=timezone.utc))
    tags:          list[str]             = field(default_factory=list)


@dataclass
class BenchmarkSuite:
    """
    Versioned, immutable collection of labelled evaluation cases.

    Once published, the suite content is frozen. Modifications require
    creating a new version.
    """
    benchmark_id:  str
    tenant_id:     str
    name:          str
    version:       str
    description:   str
    status:        BenchmarkStatus
    cases:         list[BenchmarkCase]   = field(default_factory=list)
    content_hash:  str                   = ""
    created_at:    datetime              = field(default_factory=lambda: datetime.now(tz=timezone.utc))
    created_by:    str                   = ""
    published_at:  Optional[datetime]    = None
    parent_version: Optional[str]        = None

    @property
    def case_count(self) -> int:
        return len(self.cases)

    @property
    def category_distribution(self) -> dict[str, int]:
        dist: dict[str, int] = {}
        for c in self.cases:
            dist[c.category] = dist.get(c.category, 0) + 1
        return dist

    def to_dict(self) -> dict[str, Any]:
        return {
            "benchmark_id":    self.benchmark_id,
            "tenant_id":       self.tenant_id,
            "name":            self.name,
            "version":         self.version,
            "description":     self.description,
            "status":          self.status.value,
            "case_count":      self.case_count,
            "content_hash":    self.content_hash,
            "created_at":      self.created_at.isoformat(),
            "created_by":      self.created_by,
            "published_at":    self.published_at.isoformat() if self.published_at else None,
            "category_distribution": self.category_distribution,
        }


class BenchmarkStore:
    """
    Persistent benchmark suite registry.

    Maintains the full version history of all benchmark suites for a tenant.
    Evaluations reference benchmark_id + version for reproducibility.
    """

    def __init__(self, db_writer: Optional[Callable] = None) -> None:
        self._suites:    dict[str, BenchmarkSuite] = {}
        self._by_tenant: dict[str, list[str]] = {}
        self._db_writer  = db_writer

    async def create(
        self,
        tenant_id:     str,
        name:          str,
        version:       str,
        description:   str,
        created_by:    str,
        parent_version: Optional[str] = None,
    ) -> BenchmarkSuite:
        """Create an empty benchmark suite in DRAFT status."""
        bid = str(uuid.uuid4())
        suite = BenchmarkSuite(
            benchmark_id  = bid,
            tenant_id     = tenant_id,
            name          = name,
            version       = version,
            description   = description,
            status        = BenchmarkStatus.DRAFT,
            created_by    = created_by,
            parent_version= parent_version,
        )
        self._suites[bid] = suite
        self._by_tenant.setdefault(tenant_id, []).append(bid)
        await self._persist("create", suite)
        return suite

    async def add_case(
        self,
        benchmark_id: str,
        case:         BenchmarkCase,
    ) -> BenchmarkSuite:
        """Add a case to a DRAFT suite."""
        suite = self._require_status(benchmark_id, BenchmarkStatus.DRAFT)
        suite.cases.append(case)
        return suite

    async def publish(
        self,
        benchmark_id: str,
        published_by: str,
    ) -> BenchmarkSuite:
        """
        Publish a benchmark suite.

        Computes the content hash and freezes the suite.
        Once published, cases cannot be added or removed.
        """
        suite = self._require_status(benchmark_id, BenchmarkStatus.DRAFT)
        if suite.case_count == 0:
            raise BenchmarkError("Cannot publish an empty benchmark suite")

        suite.content_hash = _hash_suite(suite)
        suite.status       = BenchmarkStatus.PUBLISHED
        suite.published_at = datetime.now(tz=timezone.utc)

        await self._persist("update", suite)
        log.info(
            "BenchmarkStore: published %s v%s (%d cases)",
            suite.name, suite.version, suite.case_count,
        )
        return suite

    async def deprecate(self, benchmark_id: str) -> BenchmarkSuite:
        suite = self._require_status(benchmark_id, BenchmarkStatus.PUBLISHED)
        suite.status = BenchmarkStatus.DEPRECATED
        await self._persist("update", suite)
        return suite

    def get(self, benchmark_id: str) -> Optional[BenchmarkSuite]:
        return self._suites.get(benchmark_id)

    def get_latest_published(
        self,
        tenant_id: str,
        name:      Optional[str] = None,
    ) -> Optional[BenchmarkSuite]:
        """Return the most recently published suite for a tenant (optionally by name)."""
        published = [
            s for s in self._suites.values()
            if s.tenant_id == tenant_id
            and s.status == BenchmarkStatus.PUBLISHED
            and (name is None or s.name == name)
        ]
        return max(published, key=lambda s: s.published_at or s.created_at) if published else None

    def list_for_tenant(
        self,
        tenant_id: str,
        status:    Optional[BenchmarkStatus] = None,
    ) -> list[BenchmarkSuite]:
        ids    = self._by_tenant.get(tenant_id, [])
        suites = [self._suites[i] for i in ids if i in self._suites]
        if status:
            suites = [s for s in suites if s.status == status]
        return sorted(suites, key=lambda s: s.created_at, reverse=True)

    def _require_status(self, benchmark_id: str, expected: BenchmarkStatus) -> BenchmarkSuite:
        suite = self._suites.get(benchmark_id)
        if suite is None:
            raise BenchmarkNotFoundError(benchmark_id)
        if suite.status != expected:
            raise BenchmarkError(
                f"Suite {benchmark_id[:8]} is {suite.status.value}, expected {expected.value}"
            )
        return suite

    async def _persist(self, op: str, suite: BenchmarkSuite) -> None:
        if self._db_writer:
            try:
                await self._db_writer(op, suite)
            except Exception as exc:
                log.error("BenchmarkStore: persist failed: %s", exc)


def _hash_suite(suite: BenchmarkSuite) -> str:
    """Deterministic content hash of a benchmark suite."""
    case_hashes = [
        hashlib.sha256(
            json.dumps(
                c.evaluation_case.input_snapshot,
                sort_keys=True, default=str,
            ).encode()
        ).hexdigest()
        for c in suite.cases
    ]
    payload = json.dumps({
        "name":       suite.name,
        "version":    suite.version,
        "case_hashes": case_hashes,
    }, sort_keys=True).encode()
    return hashlib.sha256(payload).hexdigest()


class BenchmarkNotFoundError(Exception):
    pass

class BenchmarkError(Exception):
    pass
